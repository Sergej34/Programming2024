package Homework_40;
import java.util.HashMap;
import java.util.Map;

public class WordFrequency {
    public static void main(String[] args) {
        // Текст из задания
        String text = "В 1800-х годах, в те времена, когда не было еще ни железных, ни шоссейных дорог, "
                + "ни газового, ни стеаринового света, ни пружинных низких диванов, ни мебели без лаку, "
                + "ни разочарованных юношей со стеклышками, ни либеральных философов-женщин, ни милых дам-камелий, "
                + "которых так много развелось в наше время, – в те наивные времена, когда из Москвы, выезжая в Петербург "
                + "в повозке или карете, брали с собой целую кухню домашнего приготовления, ехали восемь суток по мягкой, "
                + "пыльной или грязной дороге и верили в пожарские котлеты, в валдайские колокольчики и бублики, – когда "
                + "в длинные осенние вечера нагорали сальные свечи, освещая семейные кружки из двадцати и тридцати человек, "
                + "на балах в канделябры вставлялись восковые и спермацетовые свечи, когда мебель ставили симметрично, когда "
                + "наши отцы были еще молоды не одним отсутствием морщин и седых волос, а стрелялись за женщин и из другого угла "
                + "комнаты бросались поднимать нечаянно и не нечаянно уроненные платочки, наши матери носили коротенькие талии "
                + "и огромные рукава и решали семейные дела выниманием билетиков, когда прелестные дамы-камелии прятались от дневного "
                + "света, – в наивные времена масонских лож, мартинистов, тугендбунда, во времена Милорадовичей, Давыдовых, Пушкиных, "
                + "– в губернском городе К. был съезд помещиков, и кончались дворянские выборы.";

        // Удаление знаков препинания и приведение к нижнему регистру
        text = text.replaceAll("[^a-zA-Zа-яА-Я0-9\\s]", "").toLowerCase();

        // Разделение текста на слова
        String[] words = text.split("\\s+");

        // Карта для хранения частоты слов
        Map<String, Integer> wordFrequency = new HashMap<>();

        // Подсчёт частоты слов
        for (String word : words) {
            if (wordFrequency.containsKey(word)) {
                wordFrequency.put(word, wordFrequency.get(word) + 1);
            } else {
                wordFrequency.put(word, 1);
            }
        }

        // Вывод результата
        for (Map.Entry<String, Integer> entry : wordFrequency.entrySet()) {
            System.out.println("Слово: \"" + entry.getKey() + "\", Частота: " + entry.getValue());
        }
    }
}
